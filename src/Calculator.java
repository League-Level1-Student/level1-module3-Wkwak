
public class Calculator {
	private int a;
	private int b;

	public Calculator(int Num1, int Num2) {
		a = Num1;
		b = Num2;
	}

	public void add(int a, int b) {
		int sum = a + b;
	}

	public void multiply(int a, int b) {
		int product = a + b;
	}

	public void subtract(int a, int b) {
		int difference = a - b;
	}

	public void divide(int a, int b) {
		int quotient = a / b;
	}

}
